      <p><img src="../assets/images/author.png"/> 作者：Yanqin Zhang, Wen Wang, Sihai Zhang, Dandan Fan, Baohua Kou, Wuyang Zhou &nbsp;<img src="../assets/images/calendar.png"/> 发表时间：2016 19th International Symposium on Wireless Personal Multimedia Communications (WPMC), November 14~16 2016, Shenzhen, China.</p>
