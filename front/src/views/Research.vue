        <h4>Abstract</h4>
        <p>Human mobility is an interesting topic attracting researchers from various fields. In this paper, we investigate mobility of college students in campus during their full life cycle and find the demographic differences on the predictability of human mobility. We explore an extensive consuming record data set collected from a college campus. Specifically, our data set includes over 15 million consuming logs gathered from 4,741 students (with demographic information) from 2013 to 2016. We present and analyze the evolving patterns of mobility entropy by ages, academic majors, and genders. We find that entropy decreases with age increasing and entropy grouped by genders follows different distributions, which is opposite to previous studies. Our findings will supply novel insights in human mobility research and provide possible support for wireless networking design in campus.</p>
