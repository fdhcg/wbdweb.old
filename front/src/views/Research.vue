      <p><img src="../assets/images/author.png"/> 作者：Zhenkun Qiu, Sihai Zhang, Wuyang Zhou and Shui Yu &nbsp;<img src="../assets/images/calendar.png"/> 发表时间：IEEE INFOCOM DCPerf 2018，15-19 April 2018，Honolulu, HI，USA.</p>
      <div class="col-md-6">
        <h4>Abstract</h4>
        <p>Vehicular mobility statistics including vehicle velocity, relative velocity and link duration between vehicles may impact greatly on V2V communications and networking but existing works based on large scale real traces are rarely reported. In this paper, we firstly present the statistical analysis on this topic using the taxi traces in one metropolis of China, which reveals the practical distribution of the mobility statistics mentioned above. We propose a computation methodology with low computation complexity to approximate vehicular communication pattern by analyzing large scale vehicular trace data. By Maximum Likelihood Estimation (MLE), we conclude that the taxi velocity follows normal distribution and the relative velocity follows Logistic distribution in different disconnected distance. Moreover, the link duration is verified to comply with generalized Pareto distribution in different disconnected distance. Such findings are significant for designing practical transmission technology and protocols.</p>
