        <h4>Abstract</h4>
        <p>The ability to closely track the traffic load of base stations is very important for resource management and energy saving in green communications. Thus how to predict the future traffic accurately is critical and some recent studies show that correlation of traffic load exists among neighboring base stations. Inspired by these conclusions, this paper proposes a novel base station traffic prediction strategy which incorporates the historical information from neighboring cells. Here, particle swarm optimization based support vector regression is selected as the basic prediction model for its good performance. The neighbor set is determined by grid search and historical information length is set through testing. Based on the real network measurement, we verify the performance improvement by incorporating neighbors' impact. Also, the extra information required is not too much, which makes the search effort affordable.</p>
